        if(rowSelected != -1) {
            setNoDoc(Integer.parseInt(tblCliente.getValueAt(rowSelected, 0).toString()));
            System.out.println(getNoDoc());
            field.setText(String.valueOf(getNoDoc()));
            
            this.setVisible(false);
        }
