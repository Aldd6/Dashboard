    public void llenarTabla() {
        tableData = Cliente.visualizarClientes();
        for(Cliente cliente:tableData) {
            modeloTabla.addRow(new Object[] {
                cliente.getDocIdent(),
                cliente.getNombre(),
                cliente.getApellido()
            });
        }
    }

